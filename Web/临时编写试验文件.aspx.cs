        //得到表达式长度分集合
        var XpathLengthGroups = xpathArr.GroupBy(p => p.xpathPathArr.Length);
        //相同的最短路径
        List<string> MinPath = new List<string>();
        //开始循环分组
        foreach (var groux in XpathLengthGroups)
        {
            //对每个级别的表达式字符串进行排序
            for (int k = 0; k < groux.Key; k++)
            {
                //得到当前级别的表达式字符串保存在数组中
                var xpa = from p in groux
                          select p.xpathPathArr[k];

                //对当前表达式进行分组拿出最大的一组
                var xpaGroups = xpa.GroupBy(p => p).OrderByDescending(p => p.Count());

                //添加到表达式集合
                if (xpaGroups.Count() > 0)
                {
                    //如果这是一个有效的结果
                    if (xpaGroups.ElementAt(0).Count() > 100)
                    {
                        MinPath.Add(xpaGroups.ElementAt(0).Key);

                    }
                    else { 
                     //后面不再有相同的联接出现了
                     //直接跳出
                        break;
                    }
                }

            }


        }

        //得到结果表达式 最底层的同级别原素 即包括很多章节表的容器
        string str = string.Join("/", MinPath.ToArray());






    }
